export function composeSceneLocation(): string {
  const roll = randomInt(100);

  if (roll < 30) {
    return `${pick(SETTING_ADJECTIVES)} ${pick(ARCHITECTURE_TYPES)} ${pick(TIME_AND_WEATHER_PHRASES)}`;
  }

  if (roll < 55) {
    return `${pick(TERRAIN_FEATURES)} ${pick(MICRO_STRUCTURES)} ${pick(TIME_AND_WEATHER_PHRASES)}`;
  }

  if (roll < 75) {
    return `${pick(REGION_EPITHETS)} ${pick(ARCHITECTURE_TYPES)} ${pick(TIME_AND_WEATHER_PHRASES)}`;
  }

  if (roll < 90) {
    return `${pick(SETTING_ADJECTIVES)} ${pick(TERRAIN_FEATURES)} ${pick(MICRO_STRUCTURES)}`;
  }

  return `${pick(REGION_EPITHETS)} ${pick(TERRAIN_FEATURES)} ${pick(TIME_AND_WEATHER_PHRASES)}`;
}

