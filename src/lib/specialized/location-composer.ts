export function composeActionLocation(): string {
  const roll = randomInt(100);

  if (roll < 35) {
    return `${pick(SETTING_ADJECTIVES)} ${pick(TERRAIN_FEATURES)} ${pick(MICRO_STRUCTURES)} ${pick(ACTION_ENVIRONMENT_BEATS)}`;
  }

  if (roll < 60) {
    return `${pick(TERRAIN_FEATURES)} ${pick(ARCHITECTURE_TYPES)} ${pick(ACTION_ENVIRONMENT_BEATS)}`;
  }

  if (roll < 80) {
    return `${pick(REGION_EPITHETS)} ${pick(TERRAIN_FEATURES)} ${pick(TIME_AND_WEATHER_PHRASES)} ${pick(ACTION_ENVIRONMENT_BEATS)}`;
  }

  return `${composeSceneLocation()} ${pick(ACTION_ENVIRONMENT_BEATS)}`;
}
